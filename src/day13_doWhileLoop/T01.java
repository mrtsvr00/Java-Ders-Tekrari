package day13_doWhileLoop;

public class T01 {
    public static void main(String[] args) {

        //Soru 1- ’k’ harfinden ‘t’ harfine kadar harfleri yazdirin.





        char krk='k';

        do {
            System.out.print(krk);

            krk=(char)(krk+1);




        }while (krk<'t');

    }
}
